BROWN = [202/255, 141/255, 72/255];

def BOOT_BOX(color):
	a = CUBOID([1.8, 0.05, 4.8]);
	b = T([1, 2])([-0.05, 0.05])(CUBOID([0.05, 1.5, 4.8]));
	c = T([1, 2, 3])([-0.05, 0.05, -0.05])(CUBOID([1.9, 1.7, 0.05]));
	d = T([1, 2])([1.8, 0.05])(CUBOID([0.05, 1.7, 4.8]));
	e = T([1, 2])([-0.05, 0.05+1.5])(CUBOID([0.05, 0.2, 2.1]));
	f = T([1, 2])([-0.05, 0.05+1.7+0.01])(CUBOID([0.05, 0.3, 4.8]));
	g = T([1, 2, 3])([-0.05, 0.05+1.7+0.01, -0.05])(CUBOID([1.9, 0.3, 0.05]));
	return STRUCT([COLOR(color), a, b, c, T([3])([4.8+0.05])(c), d, e, 
		T([3])([2.1+0.6])(e), f, T([1])([1.8+0.05])(f), g, T([3])([4.8+0.05])(g), T([2])([1.7+0.01+0.3])(a), 
		T([2, 3])([0.02, 0.05])(c), T([2, 3])([0.02, 4.8])(c), T([1, 2])([-0.05, 0.02])(d), T([1, 2])([-1.8+0.05, 0.02])(d)]);


#creation and positioning

box = BOOT_BOX(BROWN);
boxes = STRUCT([T([1, 2, 3])([38, 8.2, 40]), R([1, 3])(PI/2), box, T([3])([4.8]), box]);
VIEW(boxes);